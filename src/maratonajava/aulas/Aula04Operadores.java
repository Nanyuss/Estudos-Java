package maratonajava.aulas;

public class Aula04Operadores {
    public static void main(String[] args) {
        /*
         + -> Adição
         - -> Subitração
         * -> Multiplicação
         / -> Divisão
         */

         int numero01 = 100;
         int numero02 = 55;
         double resultado = numero01/numero02; // Divisão de dois numeros inteiros vai gerar um numero inteiro. Para gerar um numero quebrado pelo menos um deles tem que ser do tipo double/float

         System.out.println(resultado);
    }
}